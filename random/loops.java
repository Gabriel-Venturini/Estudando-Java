package random;
public class loops {
    public static void main(String[] args) {
        // for loop

        for (int i = 0; i <= 10; i++) {
            System.out.println(i);
        } // print 0 - 10

        // while loop
        int j = 0;

        while (j != 10) {
            System.out.println(j);
            j++;
        } // print 0 - 9
    }
}
